#include <stdio.h>

int main() {
    int i = 10;
    int *p;
    //&运算符出现在非定义语句中，表示取地址的意思
    p = &i;
    //对指针变量赋值可以修改指针的指向
    //int *p = &i;
    printf("*p = %d\n",*p);
    //*运算符在定义语句中，用了说明创建指针变量
    //*运算符出现在非定义语句中，解引用/间接访问
    //-根据指针变量里面存储的地址和指针变量的基类型，去访问目标的内容
    *p  = 11;
    printf("i = %d\n",i);
    return 0;
}
